                _spConn = lplr.CharacterAdded:Connect(function()
                    if _sp then _applySpeed(true) end
                end)
            end
        else
            _sp = false
            _applySpeed(false)
            if _spConn then _spConn:Disconnect(); _spConn = nil end
        end
    end,
    Tooltip = "Increase walkspeed to 30"
})

