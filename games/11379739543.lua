local function DoIHaveBomb()
    local Char = lplr.Character
    if not Char then return false end
    for _, v in pairs(Char:GetChildren()) do
        if v:IsA("BillboardGui") or v:IsA("Highlight") then
            return true 
        end
    end
    for _, v in pairs(Char:GetChildren()) do
        if v:IsA("Tool") and (v.Name:lower():find("bomb") or v.Name:lower():find("tnt")) then
            return true
        end
    end
    return false
end

local function GetNearestPlayer()
    local MyChar = lplr.Character
    local MyRoot = MyChar and MyChar:FindFirstChild("HumanoidRootPart")
    if not MyRoot then return nil end
    local ClosestDist = math.huge
    local Target = nil
    for _, v in pairs(pl:GetPlayers()) do
        if v ~= lplr and v.Character and v.Character:FindFirstChild("HumanoidRootPart") then
            local Hum = v.Character:FindFirstChild("Humanoid")
            if Hum and Hum.Health > 0 then
                local dist = (MyRoot.Position - v.Character.HumanoidRootPart.Position).Magnitude
                if dist < ClosestDist then
                    ClosestDist = dist
                    Target = v.Character.HumanoidRootPart
                end
            end
        end
    end
    return Target
end

local function SilentTeleport(root, targetPos)
    if not root then return end
    
    root.Velocity = Vector3.new(0, 0, 0)
    root.RotVelocity = Vector3.new(0, 0, 0)
    
    local owner = GetNetworkOwner(root)
    if owner and owner ~= lplr then
        SetNetworkOwner(root, lplr)
    end
    
    root.CFrame = CFrame.new(targetPos)
    
    local startPos = root.Position
    for i = 1, 3 do
        local lerpPos = startPos:Lerp(targetPos, i / 3)
        root.CFrame = CFrame.new(lerpPos)
    end
    root.CFrame = CFrame.new(targetPos)
    
    task.wait(0.01)
    root.Velocity = Vector3.new(0, 0, 0)
    root.RotVelocity = Vector3.new(0, 0, 0)
end

local function FakeMovement(root, targetPos, duration)
    if not root then return end
    
    local bv = Instance.new("BodyVelocity")
    bv.MaxForce = Vector3.new(1/0, 1/0, 1/0)
    bv.Velocity = (targetPos - root.Position).Unit * 100
    bv.Parent = root
    
    task.wait(0.05)
    bv:Destroy()
    
    root.CFrame = CFrame.new(targetPos)
end

