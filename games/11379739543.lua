local _spSpeed = 30
local function _applySpeed(enable)
    local char = lplr.Character
    if char then
        local hum = char:FindFirstChildOfClass("Humanoid")
        if hum then
            hum.WalkSpeed = enable and _spSpeed or 16
        end
    end
end
