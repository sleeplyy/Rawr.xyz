local _esp = false
local _espConn = nil
local function _makeGrid(part)
    if not part:FindFirstChild("RawrGrid") then
        local box = Instance.new("SelectionBox")
        box.Name = "RawrGrid"
        box.Adornee = part
        box.Color3 = Color3.fromRGB(255, 0, 0)
        box.LineThickness = 0.04
        box.Transparency = 0
        box.SurfaceColor3 = Color3.fromRGB(255, 0, 0)
        box.SurfaceTransparency = 1
        box.Parent = part
    end
end
local function _updateESP()
    if _esp then
        for _, player in pairs(pl:GetPlayers()) do
            if player ~= lplr and player.Character then
                for _, part in pairs(player.Character:GetChildren()) do
                    if part:IsA("BasePart") and part.Name ~= "HumanoidRootPart" then
                        _makeGrid(part)
                    end
                end
            end
        end
    else
        for _, player in pairs(pl:GetPlayers()) do
            if player.Character then
                for _, part in pairs(player.Character:GetDescendants()) do
                    if part.Name == "RawrGrid" then part:Destroy() end
                end
            end
        end
    end
end
local ESP = vape.Categories.Render:CreateModule({
    Name = "Grid ESP",
    Function = function(callback)
        if callback then
            _esp = true
            if not _espConn then
                _espConn = rs.RenderStepped:Connect(_updateESP)
            end
        else
            _esp = false
            if _espConn then _espConn:Disconnect(); _espConn = nil end
            _updateESP()
        end
    end,
    Tooltip = "FullвҖ‘body red line ESP on enemies"
})

