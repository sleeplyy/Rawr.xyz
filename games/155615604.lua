        end
        local backpack = lplr and lplr.Backpack
        if backpack then
            local children = backpack:GetChildren()
            for i = 1, #children do itemAdded(children[i]) end
        end

        OwnShotgun = false
        OwnSniper = false
        Sniper = nil
        Shotgun = nil
    end

