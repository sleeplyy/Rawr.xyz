                           entitylib.character.Character:FindFirstChildOfClass("Tool"):GetAttribute("Local_CurrentAmmo") > 0)
                           or attempts > 10
                end
            end
        end)

        toolConns[v] = conn

        v.Destroying:Connect(function()
            if toolConns and toolConns[v] then
                pcall(function() toolConns[v]:Disconnect() end)
                toolConns[v] = nil
            end
        end)
