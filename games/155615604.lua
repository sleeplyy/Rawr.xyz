                    pcall(function() reloadConnection:Disconnect() end)
                    reloadConnection = nil
                end
                if backpackConn then
                    pcall(function() backpackConn:Disconnect() end)
                    backpackConn = nil
                end
                clearToolConns()
            end
        end
    })
