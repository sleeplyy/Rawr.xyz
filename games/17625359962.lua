        return {r = c.R, g = c.G, b = c.B}
    end

    local function tbl2col(t)
        if type(t) == "table" and t.r and t.g and t.b then
            return Color3.new(t.r, t.g, t.b)
        end
        return Color3.new(1,1,1)
    end

