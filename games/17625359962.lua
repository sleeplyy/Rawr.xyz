        for _, pname in ipairs(bodyParts) do
            local p = char:FindFirstChild(pname)
            if p and p:IsA("BasePart") then
                orig[pname] = {
                    material = p.Material,
                    color = p.Color,
                    transparency = p.Transparency
                }
            end
        end
    end

